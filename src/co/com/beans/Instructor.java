package co.com.beans;


public class Instructor {

	private int id;
	private String Nombre;
	
	public Instructor() {
		
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int idC) {
		this.id = idC;
	}
	
	public String getNombre() {
		return Nombre;
	}
	
	public void setNombre(String nom) {
		this.Nombre = nom;
	}
	
}
